    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        // get text from textfields
        String FlightId = flightId.getText();
        String FlightName = flightName.getText();
        String Arrival = arrival.getText();
        String Depatute = depature.getText();
        String Duration = duration.getText();
        String Seats = seats.getText();
        String Fare = fare.getText();
        
        // convert dateformat to string
        DateFormat da = new SimpleDateFormat("yyyy--mm--dd");
        String Date = da.format(date.getDate());
        // verify any textfield is empty
        if(FlightName.isEmpty() || Arrival.isEmpty() || Depatute.isEmpty() || Duration.isEmpty() || Seats.isEmpty() || Fare.isEmpty() ){
            JOptionPane.showMessageDialog(null, "Please fill the all details");
        }
        else{
        // connection with database
        DbmsConnection dbcon = new DbmsConnection();
        // update flight details to flight table
        String updatecustomer = "INSERT INTO flight(flight_id,FlightName,Arrival,Depature,Duration,Seats,Fare,Date)"
                + " VALUES('"+FlightId+"','"+FlightName+"','"+Arrival+"','"+Depatute+"','"+Duration+"','"+Seats+"','"+Fare+"','"+Date+"')";
                try{
                if(dbcon.updateDatabase(updatecustomer)!=0){
                    JOptionPane.showMessageDialog(null, "Flight Details Added Sucessfully");
                    // close the current window after adding flight details into database
                    this.setVisible(false);
                }
                }
                catch(Exception e){
                    e.printStackTrace();
                }
        
        
        
        
        
        
        }  
        
    }//GEN-LAST:event_jButton1ActionPerformed
