        // auto generate flightId 
     public void autoId(){
         // select last flightid and generate Id for new flight
        String groupOrderId = "SELECT max(flight_id) FROM flight";
	DbmsConnection dbcon = new DbmsConnection();
	    try {
		ResultSet rs = dbcon.getQuertTable(groupOrderId);
                rs.next();
		if(rs.getString("max(flight_id)"
                )!=null) {
                    long id= Long.parseLong(rs.getString("max(flight_id)").substring(2));
                    id++;
                    flightId.setText("FS"+String.format("%03d",id));
                }
                else{
                    flightId.setText("FS001");
                }
		}
		catch(Exception e) {
			e.printStackTrace();
		}
    }
    
    
